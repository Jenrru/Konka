    private void btnGenerateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateActionPerformed
        if((rbrubro.isSelected()&& cbxTipoI.getSelectedIndex()>0) || (rbmarca.isSelected()&& cbxTipoI.getSelectedIndex()>0)
                ||(rbuni.isSelected() && cbxTipoI.getSelectedIndex()>0) || (rbprov.isSelected() && cbxTipoI.getSelectedIndex()>0)
                ||(this.rbsmin.isSelected())||(this.rball.isSelected())){
            JasperReport jr = null;
            String path="/GUI/Informes/Productos/JRprod.jasper";
            Map parametro =  new HashMap();        
            if(rbrubro.isSelected()){
                String seleccionado=cbxTipoI.getSelectedItem().toString().toLowerCase();
                parametro.put("parameter1", "WHERE rubro='"+cbxTipoI.getSelectedItem()+"'");                
                parametro.put("parameter2", "Ordenados "+rbrubro.getText());
                if(seleccionado.equals("semillas")||seleccionado.equals("semilla")){
                    path="/GUI/Informes/Productos/JRsemilla.jasper";
                    parametro.put("parameter1", "AND rubro='"+cbxTipoI.getSelectedItem()+"'");
                }                
            }else if(rbmarca.isSelected()){
                parametro.put("parameter1", "WHERE marca='"+cbxTipoI.getSelectedItem()+"'");
                parametro.put("parameter2", "Ordenados "+rbmarca.getText());
            }else if(rbuni.isSelected()){
                parametro.put("parameter1", "WHERE unidad='"+cbxTipoI.getSelectedItem()+"'");
                parametro.put("parameter2", "Ordenados "+rbuni.getText());
            }else if(rbsmin.isSelected()){
                parametro.put("parameter1", "WHERE stock <= stockmin ");
                parametro.put("parameter2", "Ordenados "+rbsmin.getText());
            }else if(rbprov.isSelected()){
                parametro.put("parameter1", "WHERE proveedor='"+cbxTipoI.getSelectedItem()+"'");
                parametro.put("parameter2", "Ordenados "+rbprov.getText());
            }else if(rball.isSelected()){
                parametro.put("parameter1", "");
                parametro.put("parameter2", "Inventario Completo ");
            }
            try {
                jr = (JasperReport) JRLoader.loadObject(getClass().getResource(path));
                JasperPrint jp = JasperFillManager.fillReport(jr, parametro, cc.conectar());

                JasperViewer jv = new JasperViewer(jp, false);
                jv.setVisible(true);
                jv.toFront();
                jv.setTitle("Reporte de productos");
                cc.conectar().close();
            } catch (JRException | SQLException ex ) {
                JOptionPane.showMessageDialog(this, ex.getMessage());

            }
        }else{
            JOptionPane.showMessageDialog(this,"Debe al menos seleccionar una opcion");
        } 
    }//GEN-LAST:event_btnGenerateActionPerformed

