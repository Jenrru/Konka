    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            int resp = JOptionPane.showConfirmDialog(null, "Â¿EstÃ¡ seguro de que desea anular esta venta?\n No podrÃ¡ recuperarla una vez que la anule", "Alerta!", JOptionPane.YES_NO_OPTION);
            
            if (resp == JOptionPane.YES_OPTION) {
                int filasel = tbventahecha.getSelectedRow();
                int filas = this.tbProductos.getRowCount();
                String anula = "UPDATE tb_venta SET nulo = 1 WHERE n_boleta=" + tbventahecha.getValueAt(filasel, 0) + "";                
                cc.Insert(anula);
                
                for (int i = 0; i < filas ; i++) {                    
                    String cod = this.tbProductos.getValueAt(i, 0).toString();
                    int stock = Integer.parseInt(this.tbProductos.getValueAt(i, 1).toString());
                    String stockup = "UPDATE tb_productos SET stock = stock+"+stock+" WHERE codprod = '"+cod+"'";
                    cc.Insert(stockup);
                }
                cc.cargatabla(this.Fillventa, 8, venta, tbventahecha, cc.conectar());
                cc.conectar().close();
                BD.Toast.makeText(JFBuscaVentas.this, "Venta Anulada con exito!", Toast.Style.SUCCESS).display();
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

